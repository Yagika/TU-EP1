public class Aufgabe4 {
    public static void main(String[] args) {
        int a = 1;
        int b = 3;
        int c = 5;
        System.out.println("Test");
        System.out.println(a);
        System.out.println('a');
        System.out.println("a");
        System.out.println('a' + a);
        System.out.println("a" + a);
        System.out.println(a + b + c);
        System.out.println("a + b + c");
        System.out.println("" + a + b + c);
        System.out.println("" + a + (b + c));
    }
}